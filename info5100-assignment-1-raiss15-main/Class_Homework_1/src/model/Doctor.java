public class Doctor {
    
    private String docname;
    private String docid;
    private String officeaddress;
    private String speciality;
    private String visithours;
    private String qualification;
    private String procurementyear;

    public String getDocname() {
        return docname;
    }

    public void setDocname(String docname) {
        this.docname = docname;
    }

    public String getDocid() {
        return docid;
    }

    public void setDocid(String docid) {
        this.docid = docid;
    }

    public String getOfficeaddress() {
        return officeaddress;
    }

    public void setOfficeaddress(String officeaddress) {
        this.officeaddress = officeaddress;
    }

    public String getSpeciality() {
        return speciality;
    }

    public void setSpeciality(String speciality) {
        this.speciality = speciality;
    }

    public String getVisithours() {
        return visithours;
    }

    public void setVisithours(String visithours) {
        this.visithours = visithours;
    }

    public String getQualification() {
        return qualification;
    }

    public void setQualification(String qualification) {
        this.qualification = qualification;
    }

    public String getProcurementyear() {
        return procurementyear;
    }

    public void setProcurementyear(String procurementyear) {
        this.procurementyear = procurementyear;
    }
    
    
}
