private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) viewTable.getModel();
        model.setRowCount(0);
        
        for(Person vs : userList.getUser()){
         if(vs.getNuId()!= null){
         Object[] row = new Object[4];
         row[0] = vs.getNuId();
         row[1] = vs.getUserName();
         row[2] = vs.getPassword();
         row[3] = vs.getEnabled();
         model.addRow(row);
         }
        }
            
    }
