    public void tblPopulateData(){
         DefaultTableModel dtm = (DefaultTableModel)tblChemicalDrug.getModel();
        
        dtm.setRowCount(0);
        Object[] row = new Object[2];
        int i = 0;
            for(BioChemicalCompound c : drugReg.getBioChemList().getBioChemicalCompoundList()){
                 
                 row[0] = c.getBioChemicalCompound();
                 
                    
                    DrugCompounds g=  drugReg.getDrugCompoundList().getDrugCompoundHistory().get(i);
                      
                      row[1] = g.getDrugCompound();
                      dtm.addRow(row);
                     i++;
                 }
                 
            
         
        
         
        
    }

