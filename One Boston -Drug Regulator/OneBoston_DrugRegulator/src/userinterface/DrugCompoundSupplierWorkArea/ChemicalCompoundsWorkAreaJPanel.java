    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
         String chemicalName = txtChemicalCompound.getText().trim();
         String drugName = txtDrugCompound.getText().trim();
         if(chemicalName.isEmpty())
         {
             JOptionPane.showMessageDialog(null,"Please enter the chemical name");
             return;
         }
         if(drugName.isEmpty())
         {
             JOptionPane.showMessageDialog(null, "please enter the drug name ");
             return;
         }
         ArrayList<String> chemicalCheck = new ArrayList<>();
         for(BioChemicalCompound c : drugReg.getBioChemList().getBioChemicalCompoundList())
         {
             chemicalCheck.add(c.getBioChemicalCompound().toLowerCase());
             
         }
         ArrayList<String>drugcheck= new ArrayList<>();
         for(DrugCompounds g:drugReg.getDrugCompoundList().getDrugCompoundHistory())
         {
             drugcheck.add(g.getDrugCompound().toLowerCase());
         }
         if(chemicalCheck.contains(chemicalName.toLowerCase()))
         {
             JOptionPane.showMessageDialog(null,"Chemical already exists in the drug ");
             return;
             
         }
         if(drugcheck.contains(drugName.toLowerCase()))
         {
          JOptionPane.showMessageDialog(null,"Drug Compound already exists in the drug ");
             return;   
         }
        drugReg.getBioChemList().addChemicalList().setBioChemicalCompound(chemicalName);
        drugReg.getDrugCompoundList().addDrugCompoundList().setDrugCompound(drugName);
       
        tblPopulateData();
        txtChemicalCompound.setText("");
        txtDrugCompound.setText("");
    }//GEN-LAST:event_btnAddActionPerformed

