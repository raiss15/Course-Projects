    private void btnUpdateStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateStatusActionPerformed
        // TODO add your handling code here:
      
       drugWorkRequest.setStatus("Completed");
       try{ 
           int year=DateChooser.getCalendar().get(Calendar.YEAR);
        int month=DateChooser.getCalendar().get(Calendar.MONTH);
        int day=DateChooser.getCalendar().get(Calendar.DAY_OF_MONTH);
         String result=year+"-"+month+"-"+day;
           ((DrugWorkRequest) drugWorkRequest).setDeliveryTime(result);
        txtDate.setText(result);
       }
       catch(Exception e)
       {
            JOptionPane.showMessageDialog(null, "please enter the delivery date ");
            return;
       }
       
       
        
       
        JOptionPane.showMessageDialog(null, "Request updated successfully!");
    }//GEN-LAST:event_btnUpdateStatusActionPerformed

