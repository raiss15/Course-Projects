    public void tblPopulateData() {
        DefaultTableModel dtm = (DefaultTableModel) tblDrug.getModel();

        dtm.setRowCount(0);

        for (DrugRegulator dr : es.getDrugList().getDrugList()) {

            Object[] row = new Object[2];
            row[0] = dr.getDrugId();
            row[1] = dr;
            dtm.addRow(row);

        }

    }

