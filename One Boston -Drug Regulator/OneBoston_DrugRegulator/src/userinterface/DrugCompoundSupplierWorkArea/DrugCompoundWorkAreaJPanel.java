    private void btnAddDrugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddDrugActionPerformed
        // TODO add your handling code here:

        String drugName = txtDrug.getText().trim();
        if(drugName.isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Drug name cannot be empty");
            return;
        }
        ArrayList<String> drugcheck= new ArrayList<>();
        for(DrugRegulator dreg:es.getDrugList().getDrugList())
        {
            drugcheck.add(dreg.getDrug().toLowerCase());
        }
        if(drugcheck.contains(drugName.toLowerCase()))
        {
            JOptionPane.showMessageDialog(null,"Drug name already exists");
            return;
        }

        es.getDrugList().addDrugList().setDrug(drugName);
        
        tblPopulateData();
        txtDrug.setText("");

    }//GEN-LAST:event_btnAddDrugActionPerformed

    private void btnAddChemicalCompoundActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddChemicalCompoundActionPerformed
        // TODO add your handling code here:
        int index = tblDrug.getSelectedRow();
        if(index<0)
        {
            JOptionPane.showMessageDialog(null, "please select a row ");
            return;
        }
        DrugRegulator dr = (DrugRegulator) tblDrug.getValueAt(index, 1);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        userProcessContainer.add("AddChemicalsJPanel", new ChemicalCompoundsWorkAreaJPanel(userProcessContainer, es, userAccount, drugOrg, erp, network, dr));
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnAddChemicalCompoundActionPerformed

