    private void btnSavePrescriptionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSavePrescriptionActionPerformed
        // TODO add your handling code here:

        Prescription p = new Prescription();
        p.setDiagnoseOfDisease(diagnosisComboBox.getSelectedItem().toString());
        p.setMedicine(medicationCombobox.getSelectedItem().toString());

        p.setNumberOfDosesInDay((Integer) txtNoofDose.getValue());

        p.setNumberOfDays((Integer) txtNoofDays.getValue());
        p.setNetwork(network.getName());
        
        
        
        String age = txtAge.getText();
        boolean check = true;
        try {
            Integer.parseInt(age);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Age must be integer!");
            check = false;
            return;
        }

        if (p.getDiagnoseOfDisease().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter the diagnosis!");
            check = false;
            return;
        } else if (txtNameField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter the patient name!");
            check = false;
            return;

        }
         if(((Integer) txtNoofDose.getValue())==0|| ((Integer) txtNoofDays.getValue())==0)
                {
                     JOptionPane.showMessageDialog(null, "Please enter the no of dose in a day or duration !");
             check = false;
             return;
                }
         if(DateChooserBox.getDate()==null)
         {
             JOptionPane.showMessageDialog(null, "Choose the date!");
             check = false;
             return;
         }
         if(medicationCombobox.getSelectedIndex()<=0)
         {
             JOptionPane.showMessageDialog(null, "Please select the Medication Name !");
             check = false;
         }
         if(genderComboBox.getSelectedIndex()<=0)
         {
          JOptionPane.showMessageDialog(null, "Please select the Medication Name !");
             check = false;   
             return;
         }
         if(diagnosisComboBox.getSelectedIndex()<=0)
         {
             JOptionPane.showMessageDialog(null, "please select the diagonsis name");
             check=false;
             return;
         }
        
        if (check == true) {
            
            
            org.addPrescription(p);

            PharmaWorkRequest req = new PharmaWorkRequest();
            String mn = medicationCombobox.getSelectedItem().toString();
            int qty = ((Integer) txtNoofDose.getValue()) * ((Integer) txtNoofDays.getValue());
            req.setMedicationName(mn);
            req.setQuantity(qty);
            req.setSender(userAcc);
            req.setStatus("Sent");
            JOptionPane.showMessageDialog(null, "Prescription added successfully");

            Organization org = null;
            ArrayList<Enterprise> epList = network.getEnterpriseDirectory().getEnterpriseList();          
                for (Enterprise enterprise: epList) {
                    for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                        if (organization instanceof PharmaOrganization) {
                            org = organization;
                            break;
                        }
                    }
                }
            
            if (org != null) {

                org.getWorkQueue().getWorkRequestList().add(req);
                userAcc.getWorkQueue().getWorkRequestList().add(req);
            }
            
           

            tblPopulateWorkRequest();
            saveRecord(p.getNetwork(),p.getDiagnoseOfDisease(),p.getMedicine());
            txtNameField.setText("");
            txtAge.setText("");
             txtNoofDose.setValue(0);
             txtNoofDays.setValue(0);
             genderComboBox.setSelectedIndex(0);
             medicationCombobox.setSelectedIndex(0);
            
        }


    }//GEN-LAST:event_btnSavePrescriptionActionPerformed
    
    public void saveRecord(String network,String disease,String medicine){
        try {
            FileWriter file = new FileWriter(filePath,true);
            BufferedWriter bw = new BufferedWriter(file);
            PrintWriter pw = new PrintWriter(bw);
            pw.println(network+","+disease+","+medicine);
            pw.flush();
            pw.close();
        } catch (IOException ex) {
            java.util.logging.Logger.getLogger(DoctorPrescWorkAreaJPanel.class.getName()).log(Level.SEVERE, null, ex);
           
        }
        
    }
    
