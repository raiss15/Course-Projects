    public void tblPopulateWorkRequest() {

        DefaultTableModel model = (DefaultTableModel) tblPrescription.getModel();

        model.setRowCount(0);

        for (WorkRequest request : userAcc.getWorkQueue().getWorkRequestList()) {
            if (request instanceof PharmaWorkRequest) {
                Object[] row = new Object[4];

                String med = ((PharmaWorkRequest) request).getMedicationName();

                row[0] = (PharmaWorkRequest) request;
                int quantity = ((PharmaWorkRequest) request).getQuantity();
                row[1] = quantity;
                row[2] = request.getReceiver();
                String result = request.getStatus();
                row[3] = result == null ? "Waiting" : result;

                model.addRow(row);

            }
        }

    }


