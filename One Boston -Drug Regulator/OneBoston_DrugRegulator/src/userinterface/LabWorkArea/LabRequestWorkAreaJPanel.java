    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        btnSubmit.setEnabled(true);
        ArrayList<String> drugAvail = new ArrayList<>();
        Patient p = ((LabTestWorkRequest)req).getPatient();
        ArrayList<DrugCompounds> drugCompoundHistory = p.getDrugHistory().getDrugCompoundHistory();
        for (DrugCompounds g : drugCompoundHistory) {
            drugAvail.add(g.getDrugCompound());

        }
        if (drugAvail.size() <= 0) {
            JOptionPane.showMessageDialog(null, "No drugs is available!");
            return;
        }
        req.setStatus("Complete");
        DrugWorkRequest dWR = new DrugWorkRequest();
        Patient patient = ((LabTestWorkRequest) req).getPatient();
        dWR.setPatient(patient);

        dWR.setSender(uA);
        ArrayList<WorkRequest> workRequestList = uA.getWorkQueue().getWorkRequestList();
        workRequestList.add(dWR);
        ArrayList<Enterprise> enterpriseList = nW.getEnterpriseDirectory().getEnterpriseList();
        for (Enterprise ep : enterpriseList) {
            ArrayList<Organization> organizationList = ep.getOrganizationDirectory().getOrganizationList();
            for (Organization org : organizationList) {
                if (org.getName().equals("Drug Organization")) {
                    ArrayList<WorkRequest> workRequestList1 = org.getWorkQueue().getWorkRequestList();
                    workRequestList1.add(dWR);
                }
            }

        }
        JOptionPane.showMessageDialog(null, "Result has been succesfully submited");
        btnSubmit.setEnabled(false);


    }//GEN-LAST:event_btnSubmitActionPerformed

