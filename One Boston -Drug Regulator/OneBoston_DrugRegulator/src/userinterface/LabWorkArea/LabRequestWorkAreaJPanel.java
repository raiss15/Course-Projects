    private void btnCreateDrugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateDrugActionPerformed
        // TODO add your handling code here:
        Patient patient = ((LabTestWorkRequest) req).getPatient();
        String drugName = txtDrugC.getText().trim();
        if (drugName.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please add the Drug!");
            return;
        }

        ArrayList<String> drugAvail = new ArrayList<>();
        ArrayList<DrugCompounds> drugCompoundHistory = patient.getDrugHistory().getDrugCompoundHistory();
        for (DrugCompounds g1 : drugCompoundHistory) {
            drugAvail.add(g1.getDrugCompound().toLowerCase());
        }
        if (drugAvail.contains(drugName.toLowerCase())) {
            JOptionPane.showMessageDialog(null, "Drug is already in the cart!");
            return;
        }
        DrugCompounds dc = patient.getDrugHistory().addDrugCompoundList();
        dc.setDrugCompound(drugName);
        req.setPatient(patient);
        tblPopulate();
        saveRecord(dc.getDrugCompound());
        txtDrugC.setText("");

    }//GEN-LAST:event_btnCreateDrugActionPerformed

