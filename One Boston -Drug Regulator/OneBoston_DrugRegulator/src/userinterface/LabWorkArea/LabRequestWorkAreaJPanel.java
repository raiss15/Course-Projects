    public void saveRecord(String drug) {
        try {
            FileWriter files = new FileWriter(filePath, true);
            BufferedWriter buffer = new BufferedWriter(files);
            PrintWriter printer = new PrintWriter(buffer);
            printer.println(drug);
            printer.flush();
            printer.close();
        } catch (IOException ex) {
            java.util.logging.Logger.getLogger(DoctorPrescWorkAreaJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

