    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String uname = txtUsername.getText();
        char[] passArr = txtPassword.getPassword();
        String pwd = String.valueOf(passArr);
        UserAccount uAccount=system.getUserAccountDirectory().authenticateUser(uname, pwd);

        Enterprise newEnterprise=null;
        Organization newOrgranization=null;
        Network newNetwork=null;
        
        if(uAccount==null){
            for(Network n:system.getNetworkList()){
                for(Enterprise e:n.getEnterpriseDirectory().getEnterpriseList()){
                    uAccount=e.getUserAccountDirectory().authenticateUser(uname, pwd);
                    if(uAccount==null){
                       for(Organization org:e.getOrganizationDirectory().getOrganizationList()){
                           uAccount=org.getUserAccountDirectory().authenticateUser(uname, pwd);
                           if(uAccount!=null){
                               newEnterprise=e;
                               newOrgranization=org;
                               newNetwork = n;
                               break;
                           }
                       }
                        
                    }
                    else{
                       newEnterprise=e;
                       break;
                    }
                    if (newOrgranization != null) {
                        break;
                    }
                }
                if (newEnterprise != null) {
                    break;
                }
            }
        }
        
        if(uAccount==null){
            JOptionPane.showMessageDialog(null, "Wrong username or password!");
            return;
        }
        else{
            CardLayout layout=(CardLayout)container.getLayout();
            container.add("workArea",uAccount.getRole().createWorkArea(container, uAccount, newOrgranization, newEnterprise, system,newNetwork));
            layout.next(container);
        }

        btnLogin.setEnabled(false);
        btnLogout.setEnabled(true);
        txtUsername.setEnabled(false);
        txtPassword.setEnabled(false);
    }//GEN-LAST:event_btnLoginActionPerformed

    private void btnLogoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogoutActionPerformed
        btnLogout.setEnabled(false);
        txtUsername.setEnabled(true);
        txtPassword.setEnabled(true);
        btnLogin.setEnabled(true);

        txtUsername.setText("");
        txtPassword.setText("");

        container.removeAll();
        JPanel newJP = new JPanel();
        container.add("blank", newJP);
        CardLayout cl = (CardLayout) container.getLayout();
        cl.next(container);
        dB4OUtil.storeSystem(system);
    }//GEN-LAST:event_btnLogoutActionPerformed

