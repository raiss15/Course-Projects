public void populateEmployeeComboBox(Organization organization){
        comboBoxEmployee.removeAllItems();
        
        for (Employee ep : organization.getEmployeeDirectory().getEmployeeList()){
            comboBoxEmployee.addItem(ep);
        }
        for(Employee ep: organization.getEmployeeDirectory().getEmployeeList()){
            if(ep.getId()=='1')
                continue;
        }
    }
 public void populateOrgInComboBoxOptions() {
        comboBoxOrganization.removeAllItems();

        for (Organization org : ep.getOrganizationDirectory().getOrganizationList()) {
            comboBoxOrganization.addItem(org);
        }
        for (Organization org : ep.getOrganizationDirectory().getOrganizationList()) {
            int organizationID = org.getOrganizationID();
        }
    }
    
private void populateRoleComboBox(Organization organization){
        comboBoxRole.removeAllItems();
        for (Role role : organization.getSupportedRole()){
            comboBoxRole.addItem(role);
        }
    }
 public void populateAllTypeOfData() {

        DefaultTableModel dtm = (DefaultTableModel) tblUsers.getModel();

        dtm.setRowCount(0);

        for (Organization org : ep.getOrganizationDirectory().getOrganizationList()) {
            for (UserAccount userAccount : org.getUserAccountDirectory().getUserAccountList()) {
                Object row[] = new Object[2];
                row[0] = userAccount;
                row[1] = userAccount.getRole();
                ((DefaultTableModel) tblUsers.getModel()).addRow(row);
            }
        }
    }


