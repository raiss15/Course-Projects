    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String uname = txtName.getText().trim();
        String pwd = txtPassword.getText();
        Organization org = (Organization) comboBoxOrganization.getSelectedItem();
        Employee emp = (Employee) comboBoxEmployee.getSelectedItem();
        Role role = (Role) comboBoxRole.getSelectedItem();
        
        ArrayList<String>pwdAvail = new ArrayList<>();
        for(UserAccount e: org.getUserAccountDirectory().getUserAccountList())
        {
            pwdAvail.add(e.getPassword());
            
        }
        if(pwdAvail.contains(uname))
        {
            JOptionPane.showMessageDialog(null,"Pwd already exists");
            return;
        }
        
        ArrayList<String>unameAvail = new ArrayList<>();
        for(UserAccount e: org.getUserAccountDirectory().getUserAccountList())
        {
            unameAvail.add(e.getUsername());
            
        }
        if(unameAvail.contains(uname))
        {
            JOptionPane.showMessageDialog(null,"UserName already exists");
            return;
        }
        if(uname.isEmpty()||pwd.isEmpty())
        {
         JOptionPane.showMessageDialog(null,"Please enter a Valid input");
            return;   
        }
        org.getUserAccountDirectory().createUserAccount(uname, pwd, emp, role);
        
        populateAllTypeOfData();
        txtName.setText("");
        txtPassword.setText("");
    }//GEN-LAST:event_btnCreateActionPerformed

