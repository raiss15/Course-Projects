    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed
        // TODO add your handling code here:
        Organization org = (Organization) comboBoxEmpOrg.getSelectedItem();
        String name = txtName.getText().trim();
        if(name.isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Please enter name!");
            return;
        }
        ArrayList<String> isNameAvailList = new ArrayList<>();
        for(Employee o:org.getEmployeeDirectory().getEmployeeList())
        {
            isNameAvailList.add(o.getName().toLowerCase());
            
        }
        if(isNameAvailList.contains(name.toLowerCase()))
        {
            JOptionPane.showMessageDialog(null,"Employee is already registered!");
            return;
        }
        org.getEmployeeDirectory().createEmployee(name);
        populateEmployeeTbl(org);
        txtName.setText("");
    }//GEN-LAST:event_addJButtonActionPerformed

