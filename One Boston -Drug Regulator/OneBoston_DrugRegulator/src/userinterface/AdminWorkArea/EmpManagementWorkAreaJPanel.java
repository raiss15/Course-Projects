 public void orgCBPopulate(){
        comboBoxOrg.removeAllItems();
        
        for (Organization org : orgDirectory.getOrganizationList()){
            comboBoxOrg.addItem(org);
        }
    }
 public void empOrgCBPopulate(){
        comboBoxEmpOrg.removeAllItems();
        
        for (Organization org : orgDirectory.getOrganizationList()){
            comboBoxEmpOrg.addItem(org);
        }
    }
 private void populateEmployeeTbl(Organization organization){
        DefaultTableModel dtm = (DefaultTableModel) tblOrg.getModel();
        
        dtm.setRowCount(0);
        
        for (Employee e : organization.getEmployeeDirectory().getEmployeeList()){
            Object[] row = new Object[2];
            row[0] = e.getId();
            row[1] = e.getName();
            dtm.addRow(row);
        }
    }

