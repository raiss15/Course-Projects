    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        
        String names = txtName.getText().trim();
        if(names.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please enter a correct network");
            return;
        
        }
        ArrayList<String> networkcheck= new ArrayList<>();
       for(Network n: business.getNetworkList())
       {
           networkcheck.add(n.getName().toLowerCase());
           
       }
        if(networkcheck.contains(names.toLowerCase()))
        {
            JOptionPane.showMessageDialog(null, "network already exist");
            return;
        }
        Network network = business.createAndAddNetwork();
        network.setName(names);
        tblPopNet();
        txtName.setText("");
    }//GEN-LAST:event_btnSubmitActionPerformed

