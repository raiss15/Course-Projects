    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed

        Network nw = (Network) networkCombo.getSelectedItem();
        Enterprise.EnterpriseType type = (Enterprise.EnterpriseType) enterpriseKindCombobox.getSelectedItem();

        if (nw == null || type == null) {
            JOptionPane.showMessageDialog(null, "Wrong Input!");
            return;
        }
        if(txtName.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please enter a right input");
            return;
        }
        String name = txtName.getText().trim();
        ArrayList<String> enterpriseCheck = new ArrayList<>();
        for(Enterprise e :nw.getEnterpriseDirectory().getEnterpriseList())
        {
            enterpriseCheck.add(e.getName());
            
        }
        if(enterpriseCheck.contains(name))
        {
            JOptionPane.showMessageDialog(null, "Enterprise already exists with the same name");
            return;
            
        }
        Enterprise enterprise = nw.getEnterpriseDirectory().createAndAddEnterprise(name, type);

        tblPopulate();
        txtName.setText("");

    }//GEN-LAST:event_btnSubmitActionPerformed

