    private void tblPopulate() {
        DefaultTableModel mod = (DefaultTableModel) tblEnterprise.getModel();

        mod.setRowCount(0);
        for (Network network : business.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                Object[] rowx = new Object[3];
                rowx[0] = enterprise.getName();
                rowx[1] = network.getName();
                rowx[2] = enterprise.getEnterpriseType().getValue();

                mod.addRow(rowx);
            }
        }
    }

    private void ComboBoxPopulate() {
        networkCombo.removeAllItems();
        enterpriseKindCombobox.removeAllItems();

        for (Network network : business.getNetworkList()) {
            networkCombo.addItem(network);
        }

        for (Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()) {
            enterpriseKindCombobox.addItem(type);
        }

    }

