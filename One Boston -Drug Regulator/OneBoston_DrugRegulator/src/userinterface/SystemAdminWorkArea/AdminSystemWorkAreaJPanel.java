    private void btnManageAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnManageAdminActionPerformed
        int i =0;
        for(Network n:business.getNetworkList() )
        {
            for(Enterprise e :n.getEnterpriseDirectory().getEnterpriseList())
            {
                i++;
                
            }
        }
        if(i<=0)
        {
            JOptionPane.showMessageDialog(null,"No enterprise is available at a moment to create a admin for the enterprise");
            return;
        }
        EnterpriseManagementAdminJPanel panel=new EnterpriseManagementAdminJPanel(workArea, business);
        workArea.add("EnterpriseManagementAdminJPanel",panel);
        CardLayout layout=(CardLayout)workArea.getLayout();
        layout.next(workArea);
    }//GEN-LAST:event_btnManageAdminActionPerformed

