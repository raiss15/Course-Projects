    public void tblPopulateData(){
        DefaultTreeModel mod=(DefaultTreeModel)jTree.getModel();
        ArrayList<Network> nL=business.getNetworkList();
        ArrayList<Enterprise> eL;
        ArrayList<Organization> oL;
        
        Network nw;
        Enterprise ep;
        Organization org;
        
        DefaultMutableTreeNode nws=new DefaultMutableTreeNode("Networks");
        DefaultMutableTreeNode rootnode=(DefaultMutableTreeNode)mod.getRoot();
        rootnode.removeAllChildren();
        rootnode.insert(nws, 0);
        
        DefaultMutableTreeNode nwN;
        DefaultMutableTreeNode epN;
        DefaultMutableTreeNode ogN;
        
        for(int p=0;p<nL.size();p++){
            nw=nL.get(p);
            nwN=new DefaultMutableTreeNode(nw.getName());
            nws.insert(nwN, p);
            
            eL=nw.getEnterpriseDirectory().getEnterpriseList();
            for(int q=0; q<eL.size();q++){
                ep=eL.get(q);
                epN=new DefaultMutableTreeNode(ep.getName());
                nwN.insert(epN, q);
                
                oL=ep.getOrganizationDirectory().getOrganizationList();
                for(int r=0;r<oL.size();r++){
                    org=oL.get(r);
                    ogN=new DefaultMutableTreeNode(org.getName());
                    epN.insert(ogN, r);
                }
            }
        }
        mod.reload();
    }
