    private void btnDropActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDropActionPerformed
        // TODO add your handling code here:
        int rowSelect = tblStudentRegisteredCourses.getSelectedRow();
        if(rowSelect < 0){
            JOptionPane.showMessageDialog(null, "Please select a course first!");
        }else{
            Course course = (Course) tblStudentRegisteredCourses.getValueAt(rowSelect,0);
            ArrayList<ProfessorProfile> professorList = business.getProfessorDirectory().getProfessorList();
            ArrayList<StudentProfile> studentList = business.getStudentDirectory().getStudentList();
            for(StudentProfile sp : studentList){
                if(sp.getNuid().equals(studentProfile.getNuid())){
                    sp.getCoursesTaken().remove(course);
                }
            }
            
            populateTable();
            JOptionPane.showMessageDialog(null, "Course dropped!");
        }
    }//GEN-LAST:event_btnDropActionPerformed

