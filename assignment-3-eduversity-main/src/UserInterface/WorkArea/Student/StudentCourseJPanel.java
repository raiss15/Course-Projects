    private void btnSubmitRatingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitRatingActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblStudentRegisteredCourses.getSelectedRow();
        if(selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select a course to give rating to the professor!");
            return;
        }else{
            DefaultTableModel model = (DefaultTableModel) tblStudentRegisteredCourses.getModel();
            Course selectedCourse = (Course) model.getValueAt(selectedRowIndex, 0);
            
            Double rating = Double.parseDouble(txtAddRatings.getText());
            selectedCourse.getProfRatingsList().add(rating);
            JOptionPane.showMessageDialog(null, "Added rating for professor "+ selectedCourse.getProfName());
            Double sum=0.0;
            for(Double d : selectedCourse.getProfRatingsList()){
                sum+=d;
            }
            selectedCourse.setProfRating(((sum)/(selectedCourse.getProfRatingsList().size())));
            txtAddRatings.setText("");
            populateTable();
        }
        
        
        
    }//GEN-LAST:event_btnSubmitRatingActionPerformed


