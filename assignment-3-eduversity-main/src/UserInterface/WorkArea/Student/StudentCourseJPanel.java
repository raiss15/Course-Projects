    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) tblStudentRegisteredCourses.getModel();
        model.setRowCount(0);
        
        for(Course c : studentProfile.getCoursesTaken()){
            Object[] row = new Object[5];
            row[0] = c;
            row[1] = c.getCourseId();
            row[2] = c.getProfName();
            row[3] = c.getCredits();
            row[4] = c.getProfRating();
            model.addRow(row);
        }
    }

