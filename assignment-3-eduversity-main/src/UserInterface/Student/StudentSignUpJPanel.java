    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        // TODO add your handling code here:
        String fName = txtFirstName.getText();
        String lName = txtLastName.getText();
        String uName = txtUserName.getText();
        String age = txtAge.getText();
        String nuid = txtNuid.getText();
        String degree = txtDegree.getText();
        String major = txtMajor.getText();
        String minor = txtMinor.getText();
        String pwd = txtPassword.getText();
        String confPwd = txtConfPassword.getText();
        if(!(pwd.equalsIgnoreCase(confPwd))){
            JOptionPane.showMessageDialog(null, "Passwords do not match!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String email = txtEmailId.getText();
        PersonDirectory personDirectory = business.getPersonDirectory();
        Person person01 = personDirectory.newPerson("Stu-"+fName);
        StudentDirectory studentDirectory = business.getStudentDirectory();
        StudentProfile studentProfile01 = studentDirectory.newStudentProfile(person01);
        studentProfile01.setAge(age);
        studentProfile01.setFirstName(fName);
        studentProfile01.setLastName(lName);
        studentProfile01.setUserName(uName);
        studentProfile01.setMajor(major);
        studentProfile01.setMinor(minor);
        studentProfile01.setNuid(nuid);
        studentProfile01.setEmailId(email);
        studentProfile01.setDegree(degree);
        UserAccountDirectory uad = business.getUserAccountDirectory();
        UserAccount ua = uad.newUserAccount(studentProfile01, uName, pwd);
        if(ua!=null){
            StudentPortalJPanel panel = new StudentPortalJPanel(workArea, business, studentProfile01);
            workArea.add("StudentPortalJPanel", panel);
            CardLayout layout = (CardLayout) workArea.getLayout();
            layout.next(workArea);
        }
        else{
            JOptionPane.showMessageDialog(null, "Student profile not created.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSignUpActionPerformed

