    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
        // TODO add your handling code here:
        //Train, Bus, Tramp, admin
        String selectedrole=(String) roleComboBox.getSelectedItem();
        if(selectedrole.equals("Train")){
         TrainJPanel panel = new  TrainJPanel(workArea, business);
         workArea.add("TrainJPanel", panel);
         CardLayout layout = (CardLayout) workArea.getLayout();
         layout.next(workArea);
        }
        else if(selectedrole.equals("Bus")){
            BusJPanel panel = new BusJPanel(workArea, business);
         workArea.add("BusJPanel", panel);
         CardLayout layout = (CardLayout) workArea.getLayout();
         layout.next(workArea);
        }
        else if(selectedrole.equals("Tramp")){
         TrampJPanel panel = new TrampJPanel(workArea, business);
         workArea.add("TrampJPanel", panel);
         CardLayout layout = (CardLayout) workArea.getLayout();
         layout.next(workArea);  
        }
        else{
            TransitAdminJPanel panel = new TransitAdminJPanel(workArea, business);
         workArea.add("TransitAdminJPanel", panel);
         CardLayout layout = (CardLayout) workArea.getLayout();
         layout.next(workArea);  
        }
    }//GEN-LAST:event_LoginButtonActionPerformed


