    private void LoginjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginjButtonActionPerformed
        // TODO add your handling code here:
        String selectedrole=(String) rolejComboBox.getSelectedItem();
        if(selectedrole.equals("Doctor")){
         DoctorJPanel panel = new DoctorJPanel(workArea, business);
         workArea.add("DoctorJPanel", panel);
         CardLayout layout = (CardLayout) workArea.getLayout();
         layout.next(workArea);
        }
        else if(selectedrole.equals("Nurse")){
            NursesJPanel panel = new NursesJPanel(workArea, business);
         workArea.add("NursesJPanel", panel);
         CardLayout layout = (CardLayout) workArea.getLayout();
         layout.next(workArea);
        }
        else if(selectedrole.equals("Rehabsupervisor")){
         RehabWardJPanel panel = new RehabWardJPanel(workArea, business);
         workArea.add("RehabWardJPanel", panel);
         CardLayout layout = (CardLayout) workArea.getLayout();
         layout.next(workArea);  
        }
        else{
            HospitalAdminJPanel panel = new HospitalAdminJPanel(workArea, business);
         workArea.add(" HospitalAdminJPanel", panel);
         CardLayout layout = (CardLayout) workArea.getLayout();
         layout.next(workArea);  
        }
    }//GEN-LAST:event_LoginjButtonActionPerformed


