    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
        // TODO add your handling code here:
        //Tenant, Owner, Commercial, admin
        String selectedrole=(String) roleComboBox.getSelectedItem();
        if(selectedrole.equals("Tenant")){
         TenantJPanel panel = new  TenantJPanel(workArea, business);
         workArea.add("TenantJPanel", panel);
         CardLayout layout = (CardLayout) workArea.getLayout();
         layout.next(workArea);
        }
        else if(selectedrole.equals("Owner")){
            OwnerJPanel panel = new OwnerJPanel(workArea, business);
         workArea.add("OwnerJPanel", panel);
         CardLayout layout = (CardLayout) workArea.getLayout();
         layout.next(workArea);
        }
        else if(selectedrole.equals("Commercial")){
         CommercialJPanel panel = new CommercialJPanel(workArea, business);
         workArea.add("CommercialJPanel", panel);
         CardLayout layout = (CardLayout) workArea.getLayout();
         layout.next(workArea);  
        }
        else{
            RealEstateAdminJPanel panel = new RealEstateAdminJPanel(workArea, business);
         workArea.add("RealEstateAdminJPanel", panel);
         CardLayout layout = (CardLayout) workArea.getLayout();
         layout.next(workArea);  
        }
    }//GEN-LAST:event_LoginButtonActionPerformed


