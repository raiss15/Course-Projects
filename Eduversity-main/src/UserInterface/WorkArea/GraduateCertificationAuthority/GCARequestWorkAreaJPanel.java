    public Double calculateGPA(){
        Double finalGPA=0.0;
        int count=0;
        for(Course c : studentProfile.getCoursesTaken()){
            for(CourseGrading cg : c.getCourseGradeList()){
                if(studentProfile.getNuid().equals(cg.getStudentId())){
                    finalGPA+= cg.getGpa();
                    count++;
                } 
            }
            
        }
        return (finalGPA/count);
    }
