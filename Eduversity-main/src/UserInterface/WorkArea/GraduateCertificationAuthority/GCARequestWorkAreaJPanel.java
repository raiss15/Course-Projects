    private void btnCalculateEligibilityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateEligibilityActionPerformed
        // TODO add your handling code here:
        boolean canGraduate = true;
        ArrayList<Course> coursesTaken = studentProfile.getCoursesTaken();
        if(!(coursesTaken.size()>7)){
            canGraduate = false;
        }
        for(Course c : coursesTaken){
            for(CourseGrading cg : c.getCourseGradeList()){
                if(studentProfile.getNuid().equals(cg.getStudentId())){
                    String finalGrade = cg.getFinalGrade();
//                    System.err.println("Checking F");
                    if(finalGrade.equals("F"))
                         canGraduate = false;
                }
            }
            
        }
        System.out.println(canGraduate);
        if(canGraduate == true){
            txtCanGraduateRequest.setText("Approve");
        }
        else{
            txtCanGraduateRequest.setText("Reject");
        }
        
        JOptionPane.showMessageDialog(null, "Eligibility has been calculated. Please press the approve or reject button as per the text box.");
        
    }//GEN-LAST:event_btnCalculateEligibilityActionPerformed

