    private void btnAddGradesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddGradesActionPerformed
        // TODO add your handling code here:
        boolean flag = false;
        ArrayList<Course> coursesTaken = studentProfile.getCoursesTaken();
        for(Course c : coursesTaken){
            for(CourseGrading cg : c.getCourseGradeList()){
                 if((c.getProfId().equals(professorProfile.getProfId()) && (cg.getStudentId().equals(studentProfile.getNuid())))){
                
                if(!txtAssgn1.getText().equals("")){
                String assgn1 = txtAssgn1.getText();
//                c.getCourseGrade().setAssignment1(assgn1);
                cg.setAssignment1(assgn1);
                flag=true;
                }
                if(!txtAssgn2.getText().equals("")){
                String assgn2 = txtAssgn2.getText();
//                c.getCourseGrade().setAssignment2(assgn2);
                cg.setAssignment2(assgn2);
                flag=true;
                }
                if(!txtAssgn3.getText().equals("")){
                String assgn3 = txtAssgn3.getText();
//                c.getCourseGrade().setAssignment3(assgn3);
                cg.setAssignment3(assgn3);
                flag=true;
                }
                if(!txtAssgn4.getText().equals("")){
                String assgn4 = txtAssgn4.getText();
//                c.getCourseGrade().setAssignment4(assgn4);
                cg.setAssignment4(assgn4);
                flag=true;
                }
                if(!txtAssgn5.getText().equals("")){
                String assgn5 = txtAssgn5.getText();
//                c.getCourseGrade().setAssignment5(assgn5);
                cg.setAssignment5(assgn5);
                flag=true;
                }
                if(!txtMidTerm.getText().equals("")){
                String midTerm = txtMidTerm.getText();
//                c.getCourseGrade().setMidTerm(midTerm);
                cg.setMidTerm(midTerm);
                flag=true;
                }
                if(!txtEndTerm.getText().equals("")){
                String endTerm = txtEndTerm.getText();
//                c.getCourseGrade().setEndTerm(endTerm);
                cg.setEndTerm(endTerm);
                flag=true;}
              
            }
            }
           
        }
        if(flag == true){
            JOptionPane.showMessageDialog(null, "Student Grades updated!");
        }else{
            JOptionPane.showMessageDialog(null, "Unable to update student grades!");
        }
        
    }//GEN-LAST:event_btnAddGradesActionPerformed


